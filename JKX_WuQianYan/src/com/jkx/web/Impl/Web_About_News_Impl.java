package com.jkx.web.Impl;

public class Web_About_News_Impl {
	
	int id;
	String newsName;
	String newsExplain;
	String newsTime;
	String newsClicks;
	String newsImg;
	String newsIntroduce;

	
	public String getNewsImg() {
		return newsImg;
	}

	public void setNewsImg(String newsImg) {
		this.newsImg = newsImg;
	}

	public String getNewsTime() {
		return newsTime;
	}
	
	public void setNewsTime(String newsTime) {
		this.newsTime = newsTime;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getNewsName() {
		return newsName;
	}

	public void setNewsName(String newsName) {
		this.newsName = newsName;
	}

	public String getNewsExplain() {
		return newsExplain;
	}

	public void setNewsExplain(String newsExplain) {
		this.newsExplain = newsExplain;
	}

	public String getNewsClicks() {
		return newsClicks;
	}

	public void setNewsClicks(String newsClicks) {
		this.newsClicks = newsClicks;
	}
	
	public String getNewsIntroduce() {
		return newsIntroduce;
	}

	public void setNewsIntroduce(String newsIntroduce) {
		this.newsIntroduce = newsIntroduce;
	}

}
